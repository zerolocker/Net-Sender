    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(jList2.getSelectedIndex()!=-1) {
            ((FileSenderListItem)jList2.getSelectedValue()).relatedProgressBar.setVisible(false);
            int a=jList2.getSelectedIndex(),height=jList2.getCellBounds(a, a).height;
            try{
               while(true){
                   JProgressBar bar=((FileSenderListItem)((DefaultListModel)(jList2.getModel())).elementAt(++a)).relatedProgressBar;
                   Rectangle r = bar.getBounds();bar.setLocation(r.x, r.y-height);
               }
            }catch(ArrayIndexOutOfBoundsException e){};
            ((DefaultListModel)(jList2.getModel())).remove(jList2.getSelectedIndex());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

