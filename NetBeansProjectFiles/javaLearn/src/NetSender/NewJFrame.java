    private void jSlider1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSlider1MouseReleased
        // TODO add your handling code here:
        JSlider s=((JSlider)evt.getSource());
        if(TransmitterManager.myplayer.isPlaying())
            {
                double rate = s.getValue() * 1.0 / s.getMaximum();
                s.setValueIsAdjusting(true);
                TransmitterManager.sendSongSeekReqAndSeek(rate);
                s.setValueIsAdjusting(false);
            }
    }//GEN-LAST:event_jSlider1MouseReleased

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if(jList3.getSelectedIndex()!=-1) {
            SongListItem item=(SongListItem)jList3.getSelectedValue();
            if(TransmitterManager.myplayer.getCurrentSongItem()!=item){
                TransmitterManager.sendRemoveSongReq(item);
                ((DefaultListModel)(jList3.getModel())).remove(jList3.getSelectedIndex());
            }else{
                JOptionPane.showMessageDialog(null, "你不能删除正在播放的歌曲");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed


