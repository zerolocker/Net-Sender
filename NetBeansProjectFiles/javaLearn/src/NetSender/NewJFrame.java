    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser=new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setMultiSelectionEnabled(true);
        chooser.setFileFilter(new FileFilter() {
            public boolean accept(File file) {return file.getName().toLowerCase().endsWith(".mp3")||file.isDirectory();}
            public String getDescription() {return "MP3(*.mp3)";}
        });
        int retval=chooser.showOpenDialog(null);
        if(retval==JFileChooser.APPROVE_OPTION) {
            try {
                TransmitterManager.addSongsAndSync(jTextArea4.getText(),
                        TransmitterManager.playerPort,
                        chooser.getSelectedFiles());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "端口必须由数字组成");
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

